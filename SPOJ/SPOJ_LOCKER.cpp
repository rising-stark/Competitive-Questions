ll solve(ll n){
	ll ans, n1;
	if(n < 3) {
		return n;
	}
	if(n%3 == 1) {
		n1 = (n - 4) / 3;
		ans = 4*binPow(3, n1);
	}
	else if(n%3 == 2){
		n1 = (n - 2) / 3;
		ans = 2*binPow(3, n1);
	}
	else 
		ans = binPow(3, n/3);
	ans %= M;
	return ans;
}