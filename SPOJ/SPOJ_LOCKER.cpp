ll dp[10000000];
ll M=mod;
ll binPow(ll a, ll n) {
	if(n == 0)
		return 1;
	if(n == 1)
		return a%M;
	ll r = binPow(a, n/2);
	if(n % 2 == 0) {
		return (r%M * r%M)%M;
	}
	else 
		return (r%M * r%M * a%M)%M;
}
