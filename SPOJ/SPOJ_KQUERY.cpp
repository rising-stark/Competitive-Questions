void build(int arr[], int si, int ss, int se){
	if(se==ss){
		st[si]=vector<int>(1, arr[ss-1]);
		return;
	}
	int mid=(ss+se)/2;
	build(arr, 2*si, ss, mid);
	build(arr, 2*si+1, mid+1, se);
	merge(ALL(st[2*si]), ALL(st[2*si+1]), back_inserter(st[si]));
}

int query(int si, int ss, int se, int i, int j, int k){
	if(i>se || j<ss){
		return 0;
	}
	if(i<=ss && se<=j){
		int ans=0;
		vector<int>::iterator it;
		it=upper_bound(ALL(st[si]), k);
		return (st[si].end()-it);
	}
	int mid=(ss+se)/2;
	int a=query(2*si, ss, mid, i, j, k);
	int b=query(2*si+1, mid+1, se, i, j, k);
	return(a+b);
}
