vvll matmult(vvll res, vvll a, ll m, ll n){
	vvll x(n, vll(n, 0));
	ll i, j, k;
	for(i=0;i<n;i++){
		for(j=0;j<n;j++){
			for(k=0;k<n;k++){
				x[i][j]=(x[i][j] + mod*m+ (res[i][k]*a[k][j])%m)%m;
			}
		}
	}
	return x;
}

void solve1(){
	ll i, j, n, m, k;
	cin>>k>>m>>n;
	if(k==0){
		ll x;
		cin>>x;
		printf("%d",(x + mod*m)%m);
		return;
	}
	vvll res(k+1, vll(k+1, 0));
	vvll a(k+1, vll(k+1, 0));
	vll b(k, 0);
	for(i=0;i<=k;i++){
		cin>>a[0][i];
		res[i][i]=1;
		if(i>0){
			a[i][i-1]=1;
		}
	}
	a[k][k]=1;
	a[k][k-1]=0;
	for(i=k-1;i>=0;i--){
		cin>>b[i];
	}

	if(n<k){
		reverse(ALL(b));
		printf("%d",(b[n] + mod*m)%m);
		return;
	}
	n-=k-1;
	while(n>0){
		if(n&1){
			res=matmult(res, a, m, k+1);
		}
		n>>=1;
		a=matmult(a, a, m, k+1);
	}
	ll ans=0;
	for(i=0;i<k;i++){
		ans=(ans+ mod*m+ (res[0][i]*b[i])%m)%m;
	}
	ans=(ans + mod*m+ res[0][k])%m;
	printf("%d",(ans + mod*m)%m);
}